export function translateCategory(category) {
  if (category === "electronics") {
    return "Electronica";
  }
  if (category === "jewelery") {
    return "Joyeria";
  }
  if (category === "men's clothing") {
    return "Ropa de hombre";
  }
  if (category === "women's clothing") {
    return "Ropa de mujer";
  }
}
